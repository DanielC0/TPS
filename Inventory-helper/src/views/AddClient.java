        // TODO add your handling code here:
	    	  
		    	if (tools.Utils.noempryString(txtName.getText(), 3) && tools.Utils.noempryString(txtCc.getText(), 3)) {
					// start insert
						ClientDAO.create(new Client(txtCc.getText(), txtName.getText(), txtPhone.getText(), txtAddress.getText()));
						AdminDAO.closeConnection(); 
						txtName.setText("");
						txtCc.setText("");
						txtPhone.setText("");
						txtAddress.setText("");
						//panel.getData();
						JOptionPane.showMessageDialog(this, "Registrado con exito");
						 
				}else {
					JOptionPane.showMessageDialog(null, "Los campos de nombre y cedula deben de estar diligenciado y con mï¿½s de 3 caracteres, Registrelo otra vez ;)","No se ha podido guardar",JOptionPane.ERROR_MESSAGE);
				
				}
		pnl.getData();
    	 
        this.dispose();
        
       
